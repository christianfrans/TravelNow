	public void search (String type, String query){
        try {
            if(query.isEmpty()){
                this.rs=controller.get_packet();
            }
            else if (type.equals("name")&& !query.isEmpty()){
                this.rs = controller.showByNamePackets(query);
            }
            
            loadtable(this.rs);
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

