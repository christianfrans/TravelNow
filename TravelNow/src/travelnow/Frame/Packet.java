    public void loadtable(ResultSet rs) {
        tb_packet.setModel(DbUtils.resultSetToTableModel(rs));
		
		TableColumnModel tcm = tb_packet.getColumnModel();
		tcm.getColumn(0).setHeaderValue("ID Paket");
		tcm.getColumn(1).setHeaderValue("Harga Paket");
		tcm.getColumn(2).setHeaderValue("Deskripsi Paket");
		tcm.getColumn(3).setHeaderValue("Nama Paket");
		tcm.getColumn(4).setHeaderValue("Tanggal Dihapus");
    }
    
